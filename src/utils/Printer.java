package utils;

public class Printer {

	public static void printSeparator() {
		System.out.println("----------------------------");
	}

	public static void showLoginHelp() {
		System.out.println("-----------help------------");
		System.out.println("register");
		System.out.println("login");
		System.out.println("exit");
		System.out.println("----------------------------");
	}

	public static void showFileHelp() {
		System.out.println("-----------help------------");
		System.out.println("cd      <'..' | dirname>");
		System.out.println("create  <filename>");
		System.out.println("open    <filename>");
		System.out.println("mkdir   <dirname>");
		System.out.println("rmd     <dirname>");
		System.out.println("rmf     <filename");
		System.out.println("logout");
		System.out.println("ls");
		System.out.println("config");
		System.out.println("exit");
		System.out.println("help to get this");
		System.out.println("----------------------------");
	}

}
